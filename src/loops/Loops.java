package loops;

public class Loops {
    public static void main(String[] args) {
        //A 1 al 10
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        System.out.println();
        int a = 0;
        while (a < 10){
            System.out.println(a++);
        }
    }
}
